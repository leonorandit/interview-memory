# 基于关键词的简单联系推断
def infer_intra_links(entries):
    links = []
    # 按类型分组
    flows = [e for e in entries if '流程' in e['type']]
    contrasts = [e for e in entries if '对比' in e['type']]
    causes = [e for e in entries if '因果' in e['type']]
    lists = [e for e in entries if '列举' in e['type']]
    
    # 流程型：尝试按标题关键词排序连接
    if len(flows) >= 2:
        for i in range(len(flows)-1):
            links.append((flows[i]['anchor'], flows[i+1]['anchor'], '流程递进'))
    
    # 对比型：成对连接
    if len(contrasts) >= 2:
        for i in range(0, min(len(contrasts)-1, 3), 2):
            links.append((contrasts[i]['anchor'], contrasts[i+1]['anchor'], '对比'))
    
    # 因果型：互相影响
    if len(causes) >= 2:
        for i in range(min(len(causes)-1, 3)):
            links.append((causes[i]['anchor'], causes[i+1]['anchor'], '因果关联'))
    
    return links

